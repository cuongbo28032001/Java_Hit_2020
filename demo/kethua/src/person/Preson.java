package person;

public class Preson {
    protected String hoTen;
    protected String ngaySinh;
    protected String queQuan;

    public Preson() {
    }

    public Preson(String hoTen, String ngaySinh, String queQuan) {
        this.hoTen = hoTen;
        this.ngaySinh = ngaySinh;
        this.queQuan = queQuan;
    }
}
